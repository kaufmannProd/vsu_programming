# шуршим циферками
a = float(input("Введите a: "))
b = float(input("Введите b: "))
c = float(input("Введите c: "))
d = float(input("Введите d: "))
f = float(input("Введите f: "))

# вычисляем вычисления
if f - d:
    print("(a * b - c) / (f - d) = ", (a * b - c) / (f - d))
else:
    print("Делить на ноль нельзя!")
